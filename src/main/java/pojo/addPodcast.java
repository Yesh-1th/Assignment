package pojo;

public class addPodcast {
    String id;
    String image;
    String title;
    String publisher;
    String thumbnail;
    int listen_score;
    String listennotes_url;
    float listen_score_global_rank;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getPublisher() {
        return publisher;
    }

    public void setPublisher(String publisher) {
        this.publisher = publisher;
    }

    public String getThumbnail() {
        return thumbnail;
    }

    public void setThumbnail(String thumbnail) {
        this.thumbnail = thumbnail;
    }

    public int getListen_score() {
        return listen_score;
    }

    public void setListen_score(int listen_score) {
        this.listen_score = listen_score;
    }

    public String getListennotes_url() {
        return listennotes_url;
    }

    public void setListennotes_url(String listennotes_url) {
        this.listennotes_url = listennotes_url;
    }

    public float getListen_score_global_rank() {
        return listen_score_global_rank;
    }

    public void setListen_score_global_rank(float listen_score_global_rank) {
        this.listen_score_global_rank = listen_score_global_rank;
    }




}
